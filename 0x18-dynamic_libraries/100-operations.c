#include <stdio.h>


/**
 * add - function add two integers
 * @a: first integer
 * @b: seconed integer
 * Return: addition of two integers
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function subtract two integers
 * @a: first integer
 * @b: seconed integer
 * Return: difference between two integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function multipy two integers
 * @a: first integer
 * @b: seconed integer
 * Return: result of multiplication of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function divide two integers
 * @a: first integer
 * @b: seconed integer
 * Return: devition of two integers
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function mod two integers
 * @a: first integer
 * @b: seconed integer
 * Return: the final result of two integers
 */

int mod(int a, int b)
{
	return (a % b);
}
